t_player	*init_player(t_map *map)
{
	t_player	*player;
	int			i;

	player = (t_player *)malloc(sizeof(t_player));
	i = 0;
	while (map->field[i] != 'P')
		i += 1;
	player->index = i;
	player->crd[X] = i % map->width * SCALER; // USED SCALER
	player->crd[Y] = i / map->width * SCALER; // USED SCALER
	player->direction = 0;
	return (player);
}